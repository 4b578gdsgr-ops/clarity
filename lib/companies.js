function searchCompanies(query) {
  const q = query.toLowerCase().trim().replace(/^\$/, '');
  if (!q) return [];
  return companies.filter(c => {
    if (c.name.toLowerCase().includes(q)) return true;
    if (c.ticker.toLowerCase() === q || c.ticker.toLowerCase().includes(q)) return true;
    if (c.sector.toLowerCase().includes(q)) return true;
    if (c.subsidiaries.some(s => s.toLowerCase().includes(q))) return true;
    if (c.id.includes(q)) return true;
    return false;
  });
}

function getCompanyById(id) {
  return companies.find(c => c.id === id) || null;
}

function getAllCompanies() {
  return companies;
}

